package com.tutorial;

public class Main {
    public static void main(String[] args) {
        
        // Operator assignment
        
        //Asignment operator jumlah
        int a = 1;
        a += 10;
        System.out.println("nilai a = " + a);
        
        //Asignment operator pengurangan
        int b = 100;
        b -= 25;
        System.out.println("nilai b = " + b);
        
        //Asignment operator perkalian
        int c = 100;
        c *= 20;
        System.out.println("nilai c = " + c);
        
        //Asignment operator pembagian
        int d = 100;
        d /= 20;
        System.out.println("nilai d = " + d);
        
        //Asignment operator modulus
        int e = 10;
        e %= 7;
        System.out.println("nilai e = " + e);
    }
}
